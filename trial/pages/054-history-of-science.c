/* Elastic modulus, history:
  Euler is often credited for formalizing the elastic modulus, inspired by Galileo's “On the Breaking of a Beam” (1638),
  which described internal forces — stresses — causing rupture.
  Galileo recognized a material constant governing fracture but lacked an explicit energy concept.
  Ancient artisans surely understood by experience that sticks break under load.
  The early treatment of stress appears dimensionless and abstract — perhaps influenced by Platonic tendencies.
  This suggests there might be earlier, lost sources on internal forces.
  Was Newton the first to treat internal forces systematically? This question marks the distinction defining “stress.”
  Earlier thinkers might have pondered internal energy release, especially given gunpowder's ancient invention.
  Someone must have connected chemical energy to explosive mechanical failure.

  Euler also noted Leibniz's belief that elasticity and acoustics are linked:
  the more rigid the material, the higher the sound frequency.
  This reflects an early understanding of the dynamical nature of elasticity.
  The search for examples of large-scale instability — perhaps the mythical Tower of Babel collapsing under wind —
  mirrors later structural analysis. Pythagoras, too, studied vibration, suggesting even older curiosity
  about why strings snap when oscillating. Could this be the earliest inquiry into fatigue and resonance?
*/

