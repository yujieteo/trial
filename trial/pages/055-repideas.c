
/* Rewriting articles: I am very concerned with regards to the articles I have written especially
since I have the experience of deleting and rewriting articles over and over
again. In this article, I will like to discuss my struggles as a fledgling
writer, in particular, my feelings towards writing and reading others in my
best faith. */

/*  Knowledge and writing, writing about what you know: The first concern I have is with regards to writing on something I am
actually knowledgeable about. I find that I cannot really write about anything
that I did not personally experience. The experience of interpreting information
derived from secondary sources feels painful to me and I get fairly anxious
when I could not contextualise particular arguments made by other people on the
appropriate interpretation of sources. However, I think I have found the ideal
threshold for me to express my views, which is in terms of the central
tendencies that is expressed outwardly based either the theory, the
phenomenology, or the general rules or thumb or heuristics associated with a
particular subject matter. */

/*  Alternative arguments, comments on trying to be generous: in particular, I will try to be excessively generous with regards to
alternative arguments. This need to be trusting with regards to the variety of
arguments helps provide some form of redundancy when I try to present my views.
It feels liberating to not be too overly concerned with small mistakes made in
favour of drawing a weaker conclusion in writing. This indication of important
tendencies helps to keep the discussions more manageable and focused on rough
indications. This comes at the cost of detail and stronger conclusions, but I
feel comforted knowing that this tradeoff is now more necessary than ever in
order to help encourage my own open mindedness.   */

/*  Considerate thinking, comments: Fundamentally, I want to be challenged to think considerately of others
when I present my viewpoints. This is to ensure that the reliability of my
writing with baser ethics as a foundation will help simplify the larger
approach in editing and representing my arguments well.  Another side to this
is that I am unconcerned with the fallacies that other people may exploit in
their reasoning to come to their conclusion, and instead I would try to
empathise with their feelings that they have with their reasoning. I hope that
with this approach that I will try to represent their arguments very charitably
and it makes me feel comfortable in not having to actively seek out malice or
ignorance in their discussion. In short, I will also want to clarify the claims
that I am not making to help better contextualise my discussion. I also hope
that in my writing, I will be keen and more exposed to other ideas. This is not
to claim that I am an open minded person, but rather I hope that it is a
consequence of what I choose to write and end up being exposed to other good
ideas. An approach that I am keen to avoid is to write concisely and without
excess. In short, I think that rambling on will help better contextualise my
thoughts more and this redundancy might be helpful to keep readers on the same
page as such. Furthermore, I think more attention has to paid to the
sensitivity of the small group of readers that read my article. Even though
this comes at the expense of coherence in writing,   I do think such
concessions are valuable for the minority and helps in keeping the larger
picture decisions as simpler customs. In particular, I hope that my writing can
rule out ambiguities through (1) a clear focus; (2) an expression in terms of
the negative, (3) the use of personal incoherence in writing to reveal the
baser instincts in my mind, (4) not forgetting that while the majority of the
people who read my work will find the excessive focus on emotions and
sensitivities to be uncompelling, but the minority that do appreciate this
point will feel satisfied. As such, the most difficult part of adopting this
writing style would be to indicate sensitive content well. In particular,
vulgarities and derogative writing will not only have to be represented well,
but also be able to cater the sensibilities of the reader in a contextually
meaningful and tasteful passion. This is one of the skills I want to achieve as
a writer.   */  

/* Politically correct terminology, some comments: in particular, I am not claiming that I think politically correct
terminology is good. I definitely think that politically correct terminology
that do not represent things well must be avoided. However, I do agree in the
general sense that writing that (1) represents things well to aid in judgement
and (2) represent things sensitively will be appropriate in my writing. In
particular, I will stick with the naturalistic appeal to my own tradition
(which is fallacy by the way) deliberately in my writing, which is the way I
have read English and that is acceptable clear representations of the topics of
discussion. I do think that the use of indications allows me to use politically
correct language, and that steps taken to contextualise these terms excessively
will be helpful to place them in context. I am hopeful that I can avoid the
pretense that I can give accurate descriptions of what is going on and only
give sensible rough indications of the truth that is comforting for the reader
to digest and process. This is certainly an objectionable point, and in fact it
is very objectionable considering that I am asking to not tell the complete
truth.  However, I think there is diminishing returns and that the approrpriate
complexity will emerge as a result of placing what I would like to share
sensitively in context through the indication of minor tendencies that serve as
a possible interpretation of the truth that points towards the complexities of
our world not in shame, but in hope and comfort.  */
