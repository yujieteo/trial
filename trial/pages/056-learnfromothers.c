/* Learning, what I learnt from my parents, I have learned to not impose ideals onto other people. They
were responsible for what they do. They sought independence from what other
people think. They valued tradition and routine in hope of a modest life. */
/* Learning, what I learnt from my teachers: I have learned to be neat and tidy. They valued consistency
and showing up regularly. They taught me to think clearly. They valued hard
work outside of writing. Humility without nitpicking was one of their core
ideals. I have learned the importance of being organised and using the
appropriate techniques for the appropriate work. I have also appreciated good,
clear, and condensed expository writing.  */
/* Learning, what I learnt from my few friends: I learned how to appreciate confidentality, how to give
and take offense, how to stand up for oneself. I have learned when to stay
silent before speaking. The diversity of talents I have come the value and
enjoy in people is thanks to them. */
/* Learning, what I have learnt from other people: From the people who think differently from me and chose a different path in
life: I have learned that diversity of thought is absolutely natural. The
health of a society arises from the diversity of life on the common basis of a
consensus of emergent morality. It is with this I understand from them that
everybody concentrate of different things. This forms the colour of much in our
world, from which I am lucky to live better than kings back then by virtue of
their diligent work in pursuit of goodness in particular parts of the world. */
